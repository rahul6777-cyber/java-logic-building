/*
Question:
Print the squares of numbers from 1 to n.

Input:
n = 5

Output:
1 4 9 16 25
*/

public class Q21SquareOfNumbers {
    public static void main(String[] args) {
        int n =5;
        int i=1;
        while(i<=n){
           System.out.println(i*i);
            i++;
        }
       

    }
}